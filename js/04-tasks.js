function getMap() {
  const arr = [];

  for (let i = 0; i < 10; i++) {
    arr.push([' ', ' ', ' ', ' ', ' ', ' ', ' ', ' ', ' ', ' ']);
  }

  for (let y = 0; y < arr.length; y++) {
    for (let x = 0; x < arr[y].length; x++) {
      if (x < y) {
        arr[y][x] = '0';
      }
      if (x > y) {
        arr[y][x] = '1';
      }
      if (x === y) {
        arr[y][x] = '*';
      }

      if (y === arr.length - x - 1) {
        arr[y][x] = '*';
      }

      if (y === 0 || x === 0) {
        arr[y][x] = '#';
      }

      if (y === arr.length - 1 || x === arr.length - 1) {
        arr[y][x] = '#';
      }
    }
  }

  showArr(arr);
}

function showArr(arr) {
  let result = '';
  for (const item of arr) {
    result += item.join('') + '\n';
  }
  console.log(result);
}

