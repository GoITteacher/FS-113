function getMax(arr) {
  let max = -Infinity;

  for (let elem of arr) {
    if (elem > max) {
      max = elem;
    }
  }

  return max;
}

console.log(getMax([1, 6, 2, 3, 78, 9, 2, 2, 12, 4, 5634]));

